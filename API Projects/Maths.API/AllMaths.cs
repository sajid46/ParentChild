using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

 public class AllMaths
 {
        public AllMaths()
        {
            
        }

        public double AddTwoNumbers(double v1, double v2)
        {
            return v1 + v2;
        }

        public double MultiplyTwoNumbers(double v1, double v2)
        {
            return v1 * v2;
        }

    public double DivideTwoNumbers(double v1, double v2)
    {
        return v1 / v2;
    }
}
